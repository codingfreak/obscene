namespace codingfreaks.obscene.Logic.Abstracts.Models
{
    /// <summary>
    /// Represents color information on (de-)serialization.
    /// </summary>
    public class ColorData
    {
        #region properties

        /// <summary>
        /// The value for red.
        /// </summary>
        public int R { get; set; }

        /// <summary>
        /// The value for green.
        /// </summary>
        public int G { get; set; }

        /// <summary>
        /// The value for blue.
        /// </summary>
        public int B { get; set; }

        /// <summary>
        /// The value for alpha.
        /// </summary>
        public int A { get; set; }

        #endregion
    }
}
